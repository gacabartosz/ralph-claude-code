"""Render a final RALPH_AUDIT_REPORT.md from a finished Ralph audit run.

Combines: transcript stats, TOOLS_AUDIT.md, ISSUES.md, COVERAGE.md,
fix_plan.md, and git log on the audit branch into one polished report
suitable for committing to the audited repo (or pasting into a PR).

Usage:
    uv run python scripts/render_audit_report.py \\
        --run-dir /path/to/.ralph-runs/audit-XXXX \\
        --worktree /path/to/.ralph-worktrees/ralph_audit-... \\
        --out /path/to/audited-repo/RALPH_AUDIT_REPORT.md
"""
from __future__ import annotations

import argparse
import json
import subprocess
from datetime import UTC, datetime
from pathlib import Path


def read_transcript(run_dir: Path) -> tuple[int, float, int, list[dict]]:
    rows = []
    total = 0.0
    errs = 0
    for line in (run_dir / "transcript.jsonl").read_text(encoding="utf-8").splitlines():
        if not line.strip():
            continue
        r = json.loads(line)
        rows.append(r)
        total += float(r.get("cost_usd", 0))
        if r.get("return_code", 0) != 0:
            errs += 1
    return len(rows), total, errs, rows


def get_branch_log(worktree: Path, max_lines: int = 50) -> str:
    res = subprocess.run(
        ["git", "-C", str(worktree), "log", "--oneline", "-n", str(max_lines)],
        capture_output=True,
        text=True,
        check=False,
    )
    return res.stdout.strip()


def get_branch_name(worktree: Path) -> str:
    res = subprocess.run(
        ["git", "-C", str(worktree), "rev-parse", "--abbrev-ref", "HEAD"],
        capture_output=True,
        text=True,
        check=False,
    )
    return res.stdout.strip()


def safe_read(path: Path) -> str:
    if not path.exists():
        return f"_(missing: {path.name})_"
    return path.read_text(encoding="utf-8")


def render(run_dir: Path, worktree: Path) -> str:
    iters, total_cost, errs, rows = read_transcript(run_dir)
    branch = get_branch_name(worktree)
    log = get_branch_log(worktree)

    last = rows[-1] if rows else {}
    avg_dur = sum(r.get("duration_s", 0) for r in rows) / max(1, len(rows))

    tools_audit = safe_read(worktree / "TOOLS_AUDIT.md")
    issues = safe_read(worktree / "ISSUES.md")
    coverage = safe_read(worktree / "COVERAGE.md")
    fix_plan = safe_read(worktree / "fix_plan.md")

    fix_done = fix_plan.count("- [x]")
    fix_todo = fix_plan.count("- [ ]")

    ts = datetime.now(UTC).strftime("%Y-%m-%d %H:%M UTC")

    return f"""# Ralph audit report — {worktree.parent.parent.name}

> Generated by `ralph-claude-code` on {ts}.
> Pattern reference: <https://ghuntley.com/ralph/>

## Run summary

| Metric | Value |
|---|---|
| Branch | `{branch}` |
| Iterations | {iters} |
| Total cost (USD-equivalent) | **${total_cost:.4f}** |
| Avg iteration duration | {avg_dur:.1f}s |
| Errors (rc != 0) | {errs} |
| fix_plan.md progress | {fix_done} done / {fix_todo} pending |
| Last iteration summary | {(last.get("summary") or "")[:200]!r} |

## Branch commit log

```
{log}
```

## ISSUES.md (from audit)

{issues}

## TOOLS_AUDIT.md (from audit)

{tools_audit}

## COVERAGE.md (from audit)

{coverage}

## fix_plan.md (final state)

{fix_plan}

---

## How this report was generated

1. The Ralph harness ran `claude -p --output-format json` in a loop inside
   `{worktree}` (a git worktree branched from main).
2. Each iteration: read PROMPT.md + fix_plan.md, did ONE thing, committed.
3. Run terminated on: `EXIT_SIGNAL: true`, iteration cap, cost cap, or
   circuit breaker.
4. This document was rendered from the run artifacts via
   `scripts/render_audit_report.py`.

To file the issues from `ISSUES.md` as real GitHub issues:

```bash
uv run python scripts/file_issues.py \\
    --issues-md {worktree}/ISSUES.md \\
    --repo gacabartosz/<your-repo> \\
    --severity P0,P1
```
"""


def main() -> None:
    ap = argparse.ArgumentParser()
    ap.add_argument("--run-dir", required=True, type=Path)
    ap.add_argument("--worktree", required=True, type=Path)
    ap.add_argument("--out", required=True, type=Path)
    args = ap.parse_args()

    md = render(args.run_dir, args.worktree)
    args.out.write_text(md, encoding="utf-8")
    print(f"Wrote {len(md)} chars to {args.out}")


if __name__ == "__main__":
    main()
